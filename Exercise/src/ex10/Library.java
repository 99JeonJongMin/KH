package ex10;

public class Library {
	String bookname;
	String author;
	String pub;
	int pages;
	int price;
	
	public Library(String bookname,String author,String pub,int pages,int price) {
		this.bookname = bookname;
		this.author = author;
		this.pub = pub;
		this.pages = pages;
		this.price = price;
	}
}
